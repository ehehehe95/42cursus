#include "libft.h"

int		ft_atoi(char *str)
{
	int	ans;
	int	sign;

	ans = 0;
	sign = 1;
	while ((*str > 8 && *str < 14) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		ans = 10 * ans + *str - '0';
		str++;
	}
	return (ans * sign);
}
